package com.napier.sem;

/**
 * Represents a country
 */
public class Countries {

    /**
     * Country code
     */
    public String code;

    /**
     * Country name
     */
    public String name;

    /**
     * Continent
     */
    public String continent;

    /**
     * Region
     */
    public String region;

    /**
     * Population
     */
    public int population;

    /**
     * Capital
     */
    public int capital;
}
