package com.napier.sem;

/**
 * Represents a language
 */
public class Language {

    /**
     * Country code
     */
    public String countryCode;

    /**
     * Language name
     */
    public String languageName;

    /**
     * Language population
     */
    public long population = -1;

    /**
     * Language percentage
     */

    public double percentage=-1.0;
}
